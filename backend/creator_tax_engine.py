#!/usr/bin/env python3
"""
크리에이터(1인 미디어) 세무 판단 엔진 — Decision Engine
========================================================
신고 전에 "판단(judgment)"이 필요한 4대 쟁점을 케이스·법령 기반으로 자동 판별한다.

설계 근거 (조세심판원 결정 + 조특법/부가세법):
  - 조심 2026중1104 / 2026서0581 : 창업중소기업 세액감면 배제 (인적용역=원시적 사업창출 X)
  - 조심 2026서0055             : 별풍선 면세 인적용역 부인 (직원 8명 + 설비 보유 → 과세)
  - 대법원 2011두11549          : 창업 여부는 외형이 아닌 실질(설립경위·거래실태)로 판단
  - 국세청 업종코드 940306(인적용역·면세) vs 921505(미디어콘텐츠창작업·과세)
  - 부가세법 §26(인적용역 면세), §21~22(외화획득 용역 영세율)
  - 조특법 §6 (창업중소기업 세액감면)
  - 2026.4 시행 : 미디어콘텐츠창작업 현금매출명세서 제출 의무

판단 결과(4 + 1):
  1) 업종코드          : 940306(면세 인적용역) vs 921505(과세사업자)
  2) 부가가치세        : 매출원천별 면세 / 과세(10%) / 영세율(0%)
  3) 창업중소기업 감면 : 적용 가능 여부 + 감면율
  4) 소득종류          : 사업소득 vs 기타소득
  5) 현금매출명세서    : 2026.4~ 제출 의무 여부
"""

from dataclasses import dataclass, field


# ── 입력: 크리에이터의 사실관계 ─────────────────────────────────────────────────

@dataclass
class CreatorFacts:
    # 인적/물적 시설 (업종코드·면세 판정의 핵심 factor — 조심2026서0055)
    has_employees: bool            # 근로자(또는 고용외 형태 인력) 고용 여부
    has_physical_facility: bool    # 별도 스튜디오·전문 방송설비 등 물적시설 보유

    # 활동의 성격 (소득종류 판정 — 계속·반복성)
    is_continuous: bool            # 계속적·반복적 콘텐츠 활동인가

    # 매출 원천 (부가세 처리의 핵심 — 원천별로 다름)
    rev_overseas_ad: int = 0       # 유튜브 애드센스 등 해외 플랫폼 광고수익(외화)
    rev_domestic_ad: int = 0       # 국내 기업 협찬·광고(원화, 국내 공급)
    rev_donation: int = 0          # 별풍선·슈퍼챗·후원금(현금성)
    rev_goods: int = 0             # 굿즈·상품 판매

    # 창업중소기업 감면 (조특법 §6)
    age: int = 30                  # 창업 당시 연령
    in_capital_overconcentration: bool = False  # 수도권과밀억제권역 內 창업 여부
    prior_same_business: bool = False           # 종전 동일업종 사업/대표 이력 (위장창업 배제)

    # 규모
    annual_revenue: int = 0        # 연 매출(부가세 간이/일반·성실신고 판정)


# ── 출력 ────────────────────────────────────────────────────────────────────────

@dataclass
class TaxDecision:
    업종코드: str = ""
    업종코드_근거: str = ""
    부가세: dict = field(default_factory=dict)        # 원천별 처리
    창업감면: dict = field(default_factory=dict)
    소득종류: str = ""
    소득종류_근거: str = ""
    현금매출명세서: str = ""
    경고: list = field(default_factory=list)


# ── 노드 1: 업종코드 (940306 면세 vs 921505 과세) ───────────────────────────────
#   근거: 조심2026서0055 — 직원 고용 + 물적시설 보유 시 '독립된 인적용역'(면세) 부인
#         근로자 없고 물적시설 없는 1인 활동 → 940306(인적용역, 부가세 면세)
def decide_업종코드(f: CreatorFacts):
    if f.has_employees or f.has_physical_facility:
        return ("921505", (
            "근로자 고용 또는 물적시설(스튜디오·전문설비) 보유 → '독립된 자격의 인적용역'으로 "
            "볼 수 없어 미디어콘텐츠창작업(과세사업자). [조심2026서0055: 직원 8명+7억 인건비+"
            "방송설비 → 면세 인적용역 부인]"
        ))
    return ("940306", (
        "근로자·물적시설 없이 본인 노무 중심의 1인 활동 → 1인미디어콘텐츠창작자(인적용역), "
        "부가가치세 면세. [부가세법 §26 ① 인적용역]"
    ))


# ── 노드 2: 부가가치세 (원천별) ─────────────────────────────────────────────────
#   - 면세사업자(940306): 모든 매출 면세 (부가세 신고의무 없음, 사업장현황신고만)
#   - 과세사업자(921505):
#       · 해외 플랫폼 광고수익(외화) → 영세율 0% (부가세법 §22 외화획득 용역)
#       · 국내 협찬·광고            → 과세 10%
#       · 별풍선·후원              → 방송용역 대가이면 과세 10% (조심2026서0055)
#       · 굿즈 판매                → 과세 10% (재화 공급)
def decide_부가세(f: CreatorFacts, 업종코드: str):
    out = {}
    if 업종코드 == "940306":
        out["전체"] = "면세 (부가세 신고의무 없음, 다음해 2월 사업장현황신고만). 근거: 인적용역 면세"
        return out
    # 과세사업자
    if f.rev_overseas_ad > 0:
        out["해외플랫폼광고(애드센스)"] = (
            f"영세율 0% — 외화로 받는 해외법인(구글) 대상 용역. 매입세액은 환급 가능. "
            f"[부가세법 §22] (금액 {f.rev_overseas_ad:,})"
        )
    if f.rev_domestic_ad > 0:
        out["국내 협찬·광고"] = f"과세 10% — 국내 공급 용역 (금액 {f.rev_domestic_ad:,})"
    if f.rev_donation > 0:
        out["별풍선·후원"] = (
            f"과세 10% — 방송용역 공급의 대가로 봄. [조심2026서0055] (금액 {f.rev_donation:,})"
        )
    if f.rev_goods > 0:
        out["굿즈 판매"] = f"과세 10% — 재화의 공급 (금액 {f.rev_goods:,})"
    if not out:
        out["전체"] = "과세사업자이나 매출 원천 미입력"
    return out


# ── 노드 3: 창업중소기업 세액감면 (조특법 §6) ──────────────────────────────────
#   근거: 조심2026중1104/2026서0581 — 인적용역(940306)은 '원시적 사업창출효과' 없어 '창업' 불인정
#         대법원2011두11549 — 외형 아닌 실질(설립경위·거래실태)로 판단
def decide_창업감면(f: CreatorFacts, 업종코드: str):
    # 1) 위장창업 배제
    if f.prior_same_business:
        return {"적용": False, "감면율": "0%",
                "근거": "종전 동일업종 사업/대표 이력 → 신규 창업(원시적 창출) 아님. [조심2026서0581]"}
    # 2) 인적용역(940306)은 사업창출효과 없음 → 배제
    if 업종코드 == "940306":
        return {"적용": False, "감면율": "0%",
                "근거": ("1인 인적용역(940306)은 '기업'활동·원시적 사업창출효과 없음 → 조특법 §6 "
                        "'창업' 불인정. [조심2026중1104] 인적·물적시설을 갖춘 사업형태(921505)로 "
                        "전환 시 재검토 가능")}
    # 3) 과세사업자(921505) + 인적·물적시설 = 실질 사업 → 감면율 산정
    if f.age <= 34:
        if not f.in_capital_overconcentration:
            rate = "100% (청년 + 수도권과밀억제권역外)"
        else:
            rate = "50% (청년 + 수도권과밀억제권역內)"
    else:
        if not f.in_capital_overconcentration:
            rate = "50% (비청년 + 수도권과밀억제권역外)"
        else:
            rate = "감면 제한 (비청년 + 수도권과밀억제권역內: 업종·매출요건 별도 충족 시 일부)"
    return {"적용": True, "감면율": rate,
            "근거": ("인적·물적시설 갖춘 미디어콘텐츠창작업(921505)으로 실질적 사업 인정. "
                    "5년간 소득세 감면 [조특법 §6]. 단, 설립경위·거래실태 실질심사 전제 "
                    "[대법원2011두11549]")}


# ── 노드 4: 소득종류 (사업소득 vs 기타소득) ────────────────────────────────────
def decide_소득종류(f: CreatorFacts):
    if f.is_continuous:
        return ("사업소득", "계속적·반복적으로 콘텐츠를 제작·수익화 → 사업소득 [소득세법 §19]")
    return ("기타소득", "일시적·우발적 수입 → 기타소득 (60% 필요경비 의제 가능) [소득세법 §21]")


# ── 노드 5: 현금매출명세서 (2026.4~) ───────────────────────────────────────────
def decide_현금명세서(f: CreatorFacts, 업종코드: str):
    if f.rev_donation > 0 or 업종코드 == "921505":
        return ("의무 — 2026.4부터 슈퍼챗·별풍선·계좌후원 등 현금성 매출은 채널명·계좌·금액 "
                "명세서 제출. 미제출 시 매출액 1% 가산세")
    return "해당 가능성 낮음 (현금성 후원 매출 없음). 단 제도 시행 후 점검 권장"


# ── 통합 판단 ───────────────────────────────────────────────────────────────────

def evaluate(f: CreatorFacts) -> TaxDecision:
    d = TaxDecision()
    d.업종코드, d.업종코드_근거 = decide_업종코드(f)
    d.부가세 = decide_부가세(f, d.업종코드)
    d.창업감면 = decide_창업감면(f, d.업종코드)
    d.소득종류, d.소득종류_근거 = decide_소득종류(f)
    d.현금매출명세서 = decide_현금명세서(f, d.업종코드)

    # 경고/리스크 플래그
    if d.업종코드 == "940306" and (f.rev_domestic_ad + f.rev_goods) > 0:
        d.경고.append("⚠ 면세(940306)로 신고했으나 국내광고·굿즈 매출 존재 → 과세사업자 전환 검토 필요")
    if f.annual_revenue >= 80_000_000:
        d.경고.append("⚠ 연매출 8천만원↑ → 부가세 일반과세자 / 성실신고확인 대상 가능 (현금매출 누락 리스크)")
    if d.업종코드 == "940306" and f.rev_overseas_ad > 0:
        d.경고.append("ℹ 해외 광고수익이 있으나 면세사업자라 영세율·매입세액 환급 불가 → 과세전환 시 환급 유불리 비교")
    return d


# ── 데모: 3가지 페르소나 ────────────────────────────────────────────────────────

def _print(title, f):
    d = evaluate(f)
    print(f"\n{'='*68}\n  {title}\n{'='*68}")
    print(f"  ① 업종코드     : {d.업종코드}")
    print(f"     └ {d.업종코드_근거}")
    print(f"  ② 부가가치세   :")
    for k, v in d.부가세.items():
        print(f"     └ [{k}] {v}")
    print(f"  ③ 창업감면     : 적용={d.창업감면['적용']} / 감면율={d.창업감면['감면율']}")
    print(f"     └ {d.창업감면['근거']}")
    print(f"  ④ 소득종류     : {d.소득종류}")
    print(f"     └ {d.소득종류_근거}")
    print(f"  ⑤ 현금명세서   : {d.현금매출명세서}")
    if d.경고:
        print(f"  🚩 리스크:")
        for w in d.경고:
            print(f"     {w}")


if __name__ == "__main__":
    # 페르소나 A: 1인 유튜버 (애드센스 위주, 직원·스튜디오 없음, 청년, 지방)
    _print("페르소나 A — 1인 유튜버 (애드센스 중심, 시설 없음, 28세, 지방)",
           CreatorFacts(has_employees=False, has_physical_facility=False, is_continuous=True,
                        rev_overseas_ad=50_000_000, rev_domestic_ad=10_000_000,
                        age=28, in_capital_overconcentration=False, annual_revenue=60_000_000))

    # 페르소나 B: 스튜디오형 크리에이터 (직원 고용 + 설비, 청년, 지방)
    _print("페르소나 B — 스튜디오 크리에이터 (직원 5명+설비, 32세, 지방)",
           CreatorFacts(has_employees=True, has_physical_facility=True, is_continuous=True,
                        rev_overseas_ad=200_000_000, rev_domestic_ad=150_000_000, rev_goods=50_000_000,
                        age=32, in_capital_overconcentration=False, annual_revenue=400_000_000))

    # 페르소나 C: 스트리머 (별풍선 위주 + 직원·설비, 종전 동일업종 법인 대표 이력)
    _print("페르소나 C — 스트리머 (별풍선 중심, 직원+설비, 종전 동일업종 대표 이력)",
           CreatorFacts(has_employees=True, has_physical_facility=True, is_continuous=True,
                        rev_donation=700_000_000, age=36, in_capital_overconcentration=True,
                        prior_same_business=True, annual_revenue=700_000_000))
